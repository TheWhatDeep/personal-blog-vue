/**
 * Bake the micro font into the atlas and return a font object
 * ({glyphs, lineHeight, spaceWidth}) compatible with Renderer.text.
 */
export function bakeMicroFont(atlas) {
	const ctx = atlas.ctx
	ctx.fillStyle = '#ffffff'
	const glyphs = {}

	const bake = (code, rows) => {
		const h = 6 // 5 rows + descender row (comma)
		const w = rows[0].length
		const slot = atlas._alloc(w, h)
		for (let y = 0; y < rows.length; y++) {
			for (let x = 0; x < rows[y].length; x++) {
				if (rows[y][x] === '#') ctx.fillRect(slot.x + x, slot.y + y, 1, 1)
			}
		}
		glyphs[code] = { region: atlas._region(slot.x, slot.y, w, h), advance: w + 1 }
	}

	for (const [ch, rows] of Object.entries(G)) {
		bake(ch.charCodeAt(0), rows)
	}
	// lowercase -> small caps
	for (let c = 97; c <= 122; c++) {
		const upper = String.fromCharCode(c - 32)
		if (G[upper]) glyphs[c] = glyphs[upper.charCodeAt(0)]
	}

	return { glyphs, lineHeight: 8, spaceWidth: 3 }
}
